
exports.today = function () { // we want to return the function rather than calling it

    let date = new Date();
    let options = {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    };

    let today = date.toLocaleDateString("en-US", options);
    return today;
}

